import pytest
from handlers.tips import calculate_tips

def test_calculate_tips_only_share_roles():
    # Только официант и бартендер (делят остаток)
    staff = [
        {"fullname": "Официант А", "role": "официант"},
        {"fullname": "Бартендер Б", "role": "бартендер"},
    ]
    result = calculate_tips(1000, staff)
    # Никто не имеет фиксированных 5%, поэтому вся сумма делится
    # Коэффициенты: официант 1.0, бартендер 1.0 => каждый получает 500
    assert result["Официант А"] == 500
    assert result["Бартендер Б"] == 500
    assert sum(result.values()) == 1000

def test_calculate_tips_with_fixed_5_each():
    # Допустим, кто-то имеет фиксированные 5% (в коде FIXED_5_EACH = [], но представим)
    # Поскольку FIXED_5_EACH пуст, тест просто проверяет отсутствие ошибок
    staff = [
        {"fullname": "Менеджер М", "role": "менеджер"},
        {"fullname": "Официант О", "role": "официант"},
    ]
    # Менеджер получает 5% от общих чаевых (фиксированная доля департамента)
    # Официант получает долю от остатка
    result = calculate_tips(2000, staff)
    # Менеджер: 5% от 2000 = 100 (фиксированная доля на всех менеджеров, но их один)
    # Остаток 1900, официант получает 1900 * 1.0 / 1.0 = 1900
    # Итого: 100 + 1900 = 2000
    assert result["Менеджер М"] == 100
    assert result["Официант О"] == 1900

def test_calculate_tips_with_mixed_roles():
    # Проверка нескольких групп
    staff = [
        {"fullname": "Хостес Х", "role": "встреч_менеджер"},
        {"fullname": "Официант О1", "role": "официант"},
        {"fullname": "Официант О2", "role": "официант"},
        {"fullname": "Бартендер Б", "role": "бартендер"},
        {"fullname": "Помощник П", "role": "помощник"},
    ]
    total = 3000
    result = calculate_tips(total, staff)
    # Хостес (встреч_менеджер) – входит в FIXED_5_DEPT -> 5% от общей суммы = 150
    # Остаток = 2850
    # Коэффициенты: официант (1.0) x2, бартендер (1.0), помощник (0.5)
    # Сумма коэффициентов = 1+1+1+0.5 = 3.5
    # Доля одного коэффициента = 2850 / 3.5 ≈ 814.2857
    # Официант: 814.2857 (округляется до 814)
    # Бартендер: 814
    # Помощник: 407
    # Проверяем целочисленные суммы (округление вниз)
    assert sum(result.values()) <= total
    assert result["Хостес Х"] == 150
    # Точные значения могут отличаться из-за округления, но проверим диапазоны
    assert 800 <= result["Официант О1"] <= 815
    assert 800 <= result["Официант О2"] <= 815
    assert 800 <= result["Бартендер Б"] <= 815
    assert 400 <= result["Помощник П"] <= 410

